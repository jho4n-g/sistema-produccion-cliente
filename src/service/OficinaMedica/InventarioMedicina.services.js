import { api } from '../api';

export const getObjs = async () => {
  try {
    const data = await api.get('/medicina/inventario');
    console.log(data);
    return data.data;
  } catch (e) {
    console.log(e);
    if (e.code == 'ERR_NETWORK') {
      return {
        ok: false,
        message: 'Error en el servidor',
      };
    }
    return e.response.data;
  }
};


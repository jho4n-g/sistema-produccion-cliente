import { api } from '../api';

export const getAllObj = async () => {
  try {
    const data = await api.get('/mantenimiento/disponibilidad-linea');
    return data.data;
  } catch (e) {
    if (e.code == 'ERR_NETWORK') {
      return {
        ok: false,
        message: 'Error en el servidor',
      };
    }
    return e.response.data;
  }
};


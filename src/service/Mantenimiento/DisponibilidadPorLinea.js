import { api } from '../api';

export const getObjPromedios = async () => {
  try {
    const data = await api.get(`/mantenimiento/disponibilidad-linea/promedios`);
    return data.data;
  } catch (e) {
    console.log(e);
    if (e.code == 'ERR_NETWORK') {
      return {
        ok: false,
        message: 'Error en el servidor',
      };
    }
    return e.response.data;
  }
};
